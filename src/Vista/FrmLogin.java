    private void BtnIniciarSecionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BtnIniciarSecionMouseClicked
ContLogin clog = new ContLogin();

        if (TxtNombreUsuario.getText().equals("")||TxtClave.getText().equals("")||
                TxtNombreUsuario.getText().equals("Nombre de Usuario")||TxtClave.getText().equals("Clave o Contraseña")) {
            JOptionPane.showMessageDialog(null, "Campos vacios", "Error de proceso", JOptionPane.WARNING_MESSAGE);
        }else{
            String Clave = ContLogin.getMD5(TxtClave.getText());  
            ContLogin.usuario = TxtNombreUsuario.getText();
            clog.password = Clave;
            int respuesta = clog.ValidarLogin();
            
            if (respuesta == 1) {
               FrmPrincipal next = new FrmPrincipal();
               next.setVisible(true);
               this.dispose();
            }
            else{
                
                JOptionPane.showMessageDialog(null,"Credenciales incorrectas");
                
            }    // TODO add your handling code here:
        }
    }//GEN-LAST:event_BtnIniciarSecionMouseClicked
