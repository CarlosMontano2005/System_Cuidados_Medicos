 void LeerArchivo(){
        try {
            Scanner input = new Scanner(new File("C://Users/pc/OneDrive/Documentos/NetBeansProjects/Expo_2022/Cuidados_Medicos/dist/lib/sysconfig.txt"));
            
            int i = 0;
            while (input.hasNextLine()){
                String line = input.nextLine(); 
                switch(i){
                    case 0:
                        ContConfiguracion.IP = line;
                        break;
                    case 1:
                        ContConfiguracion.Port = line;
                        break;
                    case 2:
                        ContConfiguracion.Database = line;
                        break;
                    case 3:
                        ContConfiguracion.User = line;
                        break;
                    case 4:
                        ContConfiguracion.Password = line;
                        break;
                    default:
                        JOptionPane.showMessageDialog(null, "Error en la lectura");
                        break;  
                }
                i++;
            }
            input.close();
        }
        catch(Exception ex){
            ex.printStackTrace();
        }
    
    }
