    void CargarTabla() {
        while (Modelo.getRowCount() > 0) {
            Modelo.removeRow(0);
        }
        try{
        ResultSet rs = ContEmpleados.CargarEmpleado();
            while (rs.next()) {
                Object[] Valores = {rs.getString("Id_Empleado"), rs.getString("Apellidos_Empleado"), rs.getString("Nombres_Empleado"), rs.getString("Fecha_Nac"), rs.getString("Direccion"), rs.getString("NIT"),
                    rs.getString("Tipo_Documento"), rs.getString("Documento"), rs.getString("Genero"), rs.getString("Estado_Civil"),  rs.getString("Empresa") };
                Modelo.addRow(Valores);
            }
        } catch (Exception e) {
        }
    }
